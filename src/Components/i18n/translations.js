export function getNestedTranslation(language, key) {
  const segments = key.split(".");
  let value = translations[language];

  for (const segment of segments) {
    if (value && Object.prototype.hasOwnProperty.call(value, segment)) {
      value = value[segment];
    } else {
      return key;
    }
  }

  return value;
}
